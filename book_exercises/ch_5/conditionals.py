## Conditional Tests
pizza_type = 'cheese'
print("Is pizza_type == 'sausage'? I predict false")
print(pizza_type == 'sausage')

print("Is pizza_type == 'cheeSe'? I predict false")
print(pizza_type == 'cheeSe')

print("Is pizza_type == 'cheese'? Of course it is!")
print(pizza_type == 'cheese')

toy = 'LEGO'
print("Is toy.lower() == 'lego'? Of course it is!")
print(toy.lower() == 'lego')

colors = ['red', 'blue', 'periwinkle']
print("Is 'green' in colors? Nope!")
print('green' in colors)

print("Is 'blue' in colors? Yep!")
print('blue' in colors)

print("Is 'blue' and 'red' in colors? Yep!")
print('blue' in colors and 'red' in colors)